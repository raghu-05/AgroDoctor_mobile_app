import React, { useState } from "react";
import {
  View,
  Text,
  StyleSheet,
  TouchableOpacity,
  ScrollView,
  ActivityIndicator,
  Alert,
  StatusBar
} from "react-native";
import { useLocalSearchParams, useRouter } from "expo-router";
import * as Sharing from "expo-sharing";
import * as Print from "expo-print";
import { MaterialIcons, Ionicons, MaterialCommunityIcons } from "@expo/vector-icons";
import api from "./services/api";
import { useTheme } from "../context/ThemeContext";

export default function TreatmentScreen() {
  const router = useRouter();
  const { colors, isDark } = useTheme();
  const params = useLocalSearchParams();

  const disease = params.disease as string;
  const severity = parseFloat(params.severity as string);

  const [language, setLanguage] = useState<"English" | "Telugu" | "Hindi">("English");
  const [treatment, setTreatment] = useState("");
  const [loading, setLoading] = useState(false);

  /* ================= FETCH TREATMENT ================= */

  const fetchTreatment = async () => {
    setLoading(true);
    setTreatment("");

    try {
      const response = await api.get("/get-treatment/", {
        params: {
          disease_name: disease,
          severity: severity,
          language: language,
        },
      });

      setTreatment(response.data.treatment_plan);
    } catch (error) {
      Alert.alert("Error", "Failed to connect to AI service. Please try again.");
      setTreatment("");
    } finally {
      setLoading(false);
    }
  };

  /* ================= PDF DOWNLOAD ================= */

  const generateHTML = () => {
    return `
      <html>
        <head>
          <meta name="viewport" content="width=device-width, initial-scale=1.0">
          <style>
            body { font-family: Helvetica, Arial, sans-serif; padding: 20px; color: #333; }
            h1 { text-align: center; color: #2e7d32; }
            .badge { background: #e8f5e9; padding: 10px; border-radius: 8px; text-align: center; margin: 20px 0; }
            p, li { line-height: 1.6; font-size: 14px; }
            strong { color: #1b5e20; }
            footer { margin-top: 40px; text-align: center; font-size: 12px; color: #999; }
          </style>
        </head>
        <body>
          <h1>ðŸŒ¿ AgroDoctor Advisory</h1>
          <div class="badge">
            <p><strong>Disease:</strong> ${disease} | <strong>Severity:</strong> ${severity}%</p>
            <p><strong>Language:</strong> ${language}</p>
          </div>
          <hr/>
          <div>
            ${treatment
              .replace(/## (.*)/g, "<h2>$1</h2>")
              .replace(/\* \*\*(.*?)\*\*/g, "<p><strong>$1</strong></p>")
              .replace(/\* (.*)/g, "<li>$1</li>")
              .replace(/\n/g, "<br/>")}
          </div>
          <footer>Generated by AgroDoctor AI â€¢ Consult an expert for critical decisions.</footer>
        </body>
      </html>
    `;
  };

  const downloadTreatmentAsPDF = async () => {
    try {
      if (!treatment) {
        Alert.alert("Empty Plan", "Please generate a treatment plan first.");
        return;
      }

      const html = generateHTML();
      const { uri } = await Print.printToFileAsync({ html, base64: false });
      await Sharing.shareAsync(uri);
    } catch (error) {
      console.log("PDF error:", error);
      Alert.alert("Export Failed", "Could not generate PDF file.");
    }
  };

  /* ================= UI ================= */

  return (
    <View style={[styles.container, { backgroundColor: colors.background }]}>
      <StatusBar barStyle={isDark ? "light-content" : "dark-content"} />

      <ScrollView contentContainerStyle={styles.scrollContent} showsVerticalScrollIndicator={false}>
        
        {/* Header */}
        <View style={styles.header}>
          <View style={[styles.iconCircle, { backgroundColor: colors.primary + '20' }]}>
            <MaterialIcons name="healing" size={36} color={colors.primary} />
          </View>
          <Text style={[styles.title, { color: colors.text }]}>Treatment Plan</Text>
          <Text style={[styles.subtitle, { color: colors.icon }]}>
            AI-generated advice for {disease}
          </Text>
        </View>

        {/* Language Selector */}
        <Text style={[styles.sectionLabel, { color: colors.text }]}>Select Language:</Text>
        <View style={[styles.langContainer, { backgroundColor: colors.card, borderColor: colors.border }]}>
          {["English", "Telugu", "Hindi"].map((lang) => (
            <TouchableOpacity
              key={lang}
              style={[
                styles.langBtn,
                language === lang && { backgroundColor: colors.primary },
              ]}
              onPress={() => setLanguage(lang as any)}
            >
              <Text
                style={[
                  styles.langText,
                  { color: language === lang ? "#fff" : colors.text },
                ]}
              >
                {lang}
              </Text>
            </TouchableOpacity>
          ))}
        </View>

        {/* Fetch Button */}
        {!treatment && (
          <TouchableOpacity 
            style={[styles.fetchBtn, { backgroundColor: colors.primary }]} 
            onPress={fetchTreatment}
            disabled={loading}
          >
            {loading ? (
              <ActivityIndicator color="#fff" />
            ) : (
              <>
                <MaterialCommunityIcons name="robot-outline" size={24} color="#fff" style={{ marginRight: 8 }} />
                <Text style={styles.fetchBtnText}>Generate with AI</Text>
              </>
            )}
          </TouchableOpacity>
        )}

        {/* Loading State */}
        {loading && (
          <View style={styles.loaderBox}>
            <Text style={[styles.loadingText, { color: colors.icon }]}>Consulting AgroDoctor AI...</Text>
          </View>
        )}

        {/* Result Area */}
        {treatment !== "" && (
          <View style={[styles.resultContainer, { backgroundColor: colors.card, borderColor: isDark ? colors.border : 'transparent' }]}>
            <ScrollView nestedScrollEnabled style={{ maxHeight: 350 }}>
              <Text style={[styles.treatmentText, { color: colors.text }]}>{treatment}</Text>
            </ScrollView>
          </View>
        )}

        {/* Action Buttons */}
        {treatment !== "" && (
          <View style={styles.actionRow}>
            <TouchableOpacity
              style={[styles.actionBtn, { backgroundColor: '#444' }]} // Dark button for contrast
              onPress={downloadTreatmentAsPDF}
            >
              <MaterialIcons name="picture-as-pdf" size={20} color="#fff" style={{ marginRight: 8 }} />
              <Text style={styles.actionBtnText}>Save PDF</Text>
            </TouchableOpacity>

            <TouchableOpacity
              style={[styles.actionBtn, { backgroundColor: colors.primary }]}
              onPress={fetchTreatment} // Regenerate
            >
              <Ionicons name="refresh" size={20} color="#fff" style={{ marginRight: 8 }} />
              <Text style={styles.actionBtnText}>Regenerate</Text>
            </TouchableOpacity>
          </View>
        )}

        {/* Back Button */}
        <TouchableOpacity style={[styles.backBtn, { marginTop: 20 }]} onPress={() => router.back()}>
          <Ionicons name="arrow-back" size={20} color={colors.icon} />
          <Text style={[styles.backBtnText, { color: colors.icon }]}>Go Back</Text>
        </TouchableOpacity>

      </ScrollView>
    </View>
  );
}

